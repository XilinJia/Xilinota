import { readFile } from 'fs-extra';
import { stripOffFrontMatter } from './frontMatter';

interface ReadmeDoc {
	title: string;
	body: string;
}

export async function readmeFileTitleAndBody(sourcePath: string): Promise<ReadmeDoc> {
	try {
		let md = await readFile(sourcePath, 'utf8');
		md = stripOffFrontMatter(md).doc.trim();
		const r = md.match(/^# (.*)/);

		if (!r) {
			throw new Error('Could not determine title');
		} else {
			const lines = md.split('\n');
			lines.splice(0, 1);
			return {
				title: r[1],
				body: lines.join('\n'),
			};
		}
	} catch (error) {
		error.message = `On ${sourcePath}: ${error.message}`;
		throw error;
	}
}

export async function readmeFileTitle(sourcePath: string) {
	const r = await readmeFileTitleAndBody(sourcePath);
	return r.title;
}

export function replaceGitHubByWebsiteLinks(md: string) {
	return md
		.replace(/https:\/\/github.com\/xilinjia\/xilinota\/blob\/main\/readme\/(.*?)\/index\.md(#[^\s)]+|)/g, '/$1/$2')
		.replace(/https:\/\/github.com\/xilinjia\/xilinota\/blob\/main\/readme\/(.*?)\.md(#[^\s)]+|)/g, '/$1/$2')
		.replace(/https:\/\/github.com\/xilinjia\/xilinota\/blob\/main\/README\.md(#[^\s)]+|)/g, '/help/$1')
		.replace(/https:\/\/raw.githubusercontent.com\/xilinjia\/xilinota\/dev\/Assets\/WebsiteAssets\/(.*?)/g, '/$1');
}
